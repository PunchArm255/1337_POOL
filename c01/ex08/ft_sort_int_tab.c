#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	sort;

	i = 0;
	while (i < size - 1)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				sort = tab[i];
				tab[i] = tab[j];
				tab[j] = sort;
			}
			j++;
		}
		i++;
	}
}
