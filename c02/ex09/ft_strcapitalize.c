char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i + 1] >= 'a' && str[i + 1] <= 'z')
			&& (str[i] >= 32 && str[i] <= 47))
			str[i + 1] = str[i + 1] - 32;
		i++;
	}
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] = str[0] - 32;
	return (str);
}
